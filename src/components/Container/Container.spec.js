/**
 * React Bootstrap Container Component.
 *
 * @name:
 * <Container></Container>
 *
 * @desc
 * Container component defines bootstrap grid layout which uses
 * 12 column system and 5 tiers.
 *
 * By the default Container width is 100%.
 * On media brakes width changes to the bootstrap v4 specs.
 * <576px = max-width: auto
 * ≥576px = max-width: 540px
 * ≥768px = max-width: 720px
 * ≥992px = max-width: 920px
 * ≥1200px = max-width: 1140px
 *
 * By default, Container uses break points, for the cases where you need full width
 * container all the time, pass property 'fluid' to the component to ignore
 * media breaks.
 *
 * @requires
 * Props: none
 *
 * @prop fluid: Boolean
 */
